    public void cargarEquipos(){
        int idGrupo = 0;
        
        if(gruposCB.getSelectedItem() != null && etapaCB.getSelectedItem() != null){
            idGrupo = Integer.parseInt((String) gruposCB.getSelectedItem());
            GrupoPK gpk = new GrupoPK(idGrupo, (String) etapaCB.getSelectedItem(), idCampeonato);
            GrupoSRV grupoSRV = new GrupoSRV();
            Grupo grupo = grupoSRV.get(gpk);
            
            DefaultTableModel model = (DefaultTableModel) equiposTBL.getModel();
            //Borrar elementos anteriores
            for(int i = model.getRowCount() - 1; i >= 0 ; i--)
                model.removeRow(i);

            int n = 1;
            for(GrupoEquipo e : grupo.getGrupoEquipoCollection())
                model.addRow(new Object[]{n++, e.getEquipo().getIdEquipo(), e.getEquipo().getNombre()});

        }
        
    }
    
    public void cargarCombosEquipos(){
        EquipoSRV equipoSRV = new EquipoSRV();
        List<Equipo> equipos = equipoSRV.getAll();
        equiposCB.removeAllItems();
        idEquipoCB.removeAllItems();
        for(Equipo e : equipos){
            equiposCB.addItem(e.getNombre());
            idEquipoCB.addItem(e.getIdEquipo().toString());
        }
    }
    
