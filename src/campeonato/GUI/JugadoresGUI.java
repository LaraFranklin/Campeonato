    public void cargarDatos(){
        
        JugadorSRV jugadorSRV = new JugadorSRV();
        DefaultTableModel model = (DefaultTableModel) jugadoresTBL.getModel();
        //Borrar elementos anteriores
        for(int i = model.getRowCount() - 1; i >= 0 ; i--)
            model.removeRow(i);
        
        int n = 1;
        for(Jugador j : jugadorSRV.getAll())
            model.addRow(new Object[]{n++, j.getIdJugador(), j.getNombre(), j.getApellidos(), j.getTelefono()});
    }
    
    
