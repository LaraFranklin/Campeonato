    private void ModBTMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModBTMActionPerformed
        // TODO add your handling code here:
        int row = jugadoresTBL.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(rootPane, "No se ha seleccionado un jugador.", "Mensaje", JOptionPane.OK_OPTION);
            return;
        }
        String id = (String) jugadoresTBL.getValueAt(row, 1);
        JugadorSRV jsrv = new JugadorSRV();
        Jugador jugador = jsrv.get(id);
        CrearJugadorGUI cjgui = new CrearJugadorGUI(null, rootPaneCheckingEnabled);
        cjgui.setLocationRelativeTo(this);
        cjgui.cargarDatos(jugador);
        cjgui.setVisible(true);
        cargarDatos();
        
    }//GEN-LAST:event_ModBTMActionPerformed

    private void DELBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DELBTNActionPerformed
        // TODO add your handling code here:
        int row = jugadoresTBL.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(rootPane, "No se ha seleccionado un jugador.", "Mensaje", JOptionPane.OK_OPTION);
            return;
        }
        String id = (String) jugadoresTBL.getValueAt(row, 1);
        JugadorSRV jsrv = new JugadorSRV();
        try {
            jsrv.del(id);
            JOptionPane.showMessageDialog(rootPane, "Se ha eliminado el jugador.", "Mensaje", JOptionPane.OK_OPTION);
            cargarDatos();
        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(rootPane, "El jugador no existe o pertenece a un equipo.", "Mensaje", JOptionPane.OK_OPTION);

        }
        cargarDatos();
        
    }//GEN-LAST:event_DELBTNActionPerformed

